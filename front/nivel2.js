// Función para fusionar las dos partituras en una sola
function fusionarPartituras() {
  const notasJ = datosJugador.song.notes;
  const notasB = datosBot.song.notes;
  const mergedNotes = [];
  
  const maxLength = Math.max(notasJ.length, notasB.length);

   for (let i = 0; i < maxLength; i++) {
    const secJ = notasJ[i] || { sectionNotes: [], typeOfSection: 0, altAnim: false, bpm: 120, changeBPM: false, sectionBeats: 3, lengthInSteps: 16, mustHitSection: false };
    const secB = notasB[i] || { sectionNotes: [], typeOfSection: 0, altAnim: false, bpm: 120, changeBPM: false, sectionBeats: 3, lengthInSteps: 16, mustHitSection: false };

    let newSection = { ...secJ };

    const combinedNotes = [
      ...(secJ.sectionNotes || []),
      ...(secB.sectionNotes || [])
    ];

    newSection.sectionNotes = combinedNotes;
    mergedNotes.push(newSection);
  }
  
  datosCancion.song.notes = mergedNotes;
}

let partituraCancion = [];

function limpiarOposiciones() {
  const mapaOpositos = { 
    'arriba': 'abajo', 
    'abajo': 'arriba', 
    'izquierda': 'derecha', 
    'derecha': 'izquierda' 
  };
  
  const filtradas = [];
  const notasPorTiempo = {};

  partituraCancion.forEach(n => {
    if(!notasPorTiempo[n.tiempo]) notasPorTiempo[n.tiempo] = [];
    notasPorTiempo[n.tiempo].push(n);
  });

  Object.keys(notasPorTiempo).forEach(tiempo => {
    const grupo = notasPorTiempo[tiempo];
    const dirsUsadasEsteInstante = [];

    grupo.forEach(nota => {
      const opuesto = mapaOpositos[nota.dir];
      if (!dirsUsadasEsteInstante.includes(opuesto)) {
        dirsUsadasEsteInstante.push(nota.dir);
        filtradas.push(nota);
      }
    });
  });

  partituraCancion = filtradas;
}

function convertirDatosNotas() {
  const mapaLanes = ['izquierda', 'abajo', 'arriba', 'derecha'];
  
  datosCancion.song.notes.forEach(seccion => {
    const esTurnoJugador = seccion.mustHitSection;
    
    seccion.sectionNotes.forEach(nota => {
      const tiempo = nota[0];
      const lane = nota[1];
      let laneFinal = -1;

      if (esTurnoJugador) {
        if (lane >= 4 && lane <= 7) {
          laneFinal = 7 - lane; 
        } else if (lane >= 0 && lane <= 3) {
          laneFinal = lane;
        }
      } else {
        if (lane >= 0 && lane <= 3) {
          laneFinal = lane;
        }
      }

      if (laneFinal !== -1) {
        partituraCancion.push({
          tiempo: tiempo,
          dir: mapaLanes[laneFinal],
          mustHit: esTurnoJugador 
        });
      }
    });
  });
  
  partituraCancion.sort((a, b) => a.tiempo - b.tiempo);
  limpiarOposiciones();
}

